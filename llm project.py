# ======================
# EXTRACT GENRE NAMES
# ======================
def extract_genres(genres_str):
    """Extract clean genre names from JSON string"""
    try:
        genres_list = ast.literal_eval(genres_str)
        return [g['name'].lower() for g in genres_list]
    except:
        return []

def get_genre_string(genres_str):
    """Get space-separated genre string"""
    try:
        genres_list = ast.literal_eval(genres_str)
        return ' '.join([g['name'] for g in genres_list])
    except:
        return ""

df['genre_list'] = df['genres'].apply(extract_genres)
df['genre_names'] = df['genres'].apply(get_genre_string)

