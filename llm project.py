def extract_genre_from_query(query):
    """Extract genre from user query"""
    query_lower = query.lower()
    for keyword, genre in GENRE_MAPPING.items():
        if keyword in query_lower:
            return genre
    return None

# ======================
# GLOBAL MEMORY FOR RECOMMENDATIONS
# ======================
recommended_movies = set()

# ======================
# ENHANCED RETRIEVAL WITH MEMORY
# ======================
def retrieve_movie(query, avoid_bollywood=False, top_n=10):
    """
    RETRIEVAL Phase of RAG:
    Retrieve best matching movie with genre filtering and memory
    """
    
    # Extract requested genre
    requested_genre = extract_genre_from_query(query)
    
    # Compute similarity scores
    q_vec = vectorizer.transform([query])
    scores = cosine_similarity(q_vec, doc_vectors)[0]
    
    # Get top candidates
    ranked_indices = scores.argsort()[::-1][:100]
    
    candidates = []
    
    for idx in ranked_indices:
        movie = df.iloc[idx]
        
        # Skip if already recommended in this session
        if movie.title in recommended_movies:
            continue
        
        # Skip Bollywood if requested
        if avoid_bollywood and movie.is_bollywood:
            continue
        
        # If genre is specified, check if movie matches
        if requested_genre:
            movie_genres = movie.genre_list
            if not any(requested_genre in g for g in movie_genres):
                continue
        
        # Add to candidates with score
        candidates.append({
            'movie': movie,
            'score': scores[idx],
            'rating': movie.vote_average,
            'idx': idx
        })
        
        if len(candidates) >= top_n:
            break
    
    if not candidates:
        return None, None, None
    
    # Sort by combination of similarity score and rating
    candidates.sort(key=lambda x: x['score'] * 0.7 + (x['rating'] / 10) * 0.3, reverse=True)
    
    # Add some randomness to top 3 to vary recommendations
    if len(candidates) >= 3:
        selected = random.choice(candidates[:3])
    else:
        selected = candidates[0]
    
    # Remember this movie
    recommended_movies.add(selected['movie'].title)
    
    return selected['movie'], requested_genre, selected['score']

