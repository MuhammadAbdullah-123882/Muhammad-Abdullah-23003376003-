def bot_response(message, history):
    """
    Main chatbot response function with RAG integration
    
    RAG Pipeline:
    1. Intent Detection
    2. RETRIEVAL: Find relevant movie using TF-IDF + Cosine Similarity
    3. AUGMENTATION: Prepare context with movie metadata
    4. GENERATION: LLM creates personalized response (or template fallback)
    """
    
    if not message or len(message.strip()) < 3:
        if USE_LLM:
            welcome = (
                "👋 Hello! I'm your **AI-powered** movie recommendation assistant.\n\n"
                "I use advanced language models to understand your preferences and suggest perfect movies!\n\n"
                "**Tell me what you're in the mood for:**\n"
                "• \"I want something funny and lighthearted\"\n"
                "• \"Recommend a thrilling action movie\"\n"
                "• \"Something romantic but not cheesy\"\n"
                "• \"A mind-bending sci-fi film\"\n\n"
                "💡 I'll give you personalized recommendations with natural explanations!"
            )
        else:
            welcome = (
                "👋 Hello! I'm your movie recommendation assistant.\n\n"
                "Tell me what kind of movie you want to watch!\n\n"
                "**Examples:**\n"
                "• Action movie\n"
                "• Romantic comedy\n"
                "• Horror film\n"
                "• Sci-fi adventure\n"
                "• Thriller movie"
            )
        return welcome

    msg = message.lower()

    # Check for reset command
    if "reset" in msg or "clear history" in msg:
        recommended_movies.clear()
        return "✅ Recommendation history cleared! You can now get fresh recommendations."

    # Check for genre intent
    if not any(word in msg for word in INTENT_KEYWORDS):
        return (
            "🤔 Please specify a genre or mood.\n\n"
            "**Examples:**\n"
            "• Recommend an action movie\n"
            "• I want a comedy film\n"
            "• Show me a horror movie\n"
            "• Suggest a romantic film"
        )

    # Check if user wants to avoid Bollywood
    avoid_bollywood = "not bollywood" in msg or "no bollywood" in msg or "avoid bollywood" in msg

    # RETRIEVAL Phase: Get movie recommendation
    movie, genre, similarity_score = retrieve_movie(message, avoid_bollywood)

    if movie is None:
        return (
            "❌ Sorry, I couldn't find a new movie matching your request.\n\n"
            f"(I've already recommended {len(recommended_movies)} movies)\n\n"
            "Try: a different genre or say 'reset' to clear history"
        )

    # AUGMENTATION + GENERATION Phase: Create response
    # This calls either LLM (if available) or template (fallback)
    response = generate_llm_response(message, movie, genre, similarity_score)
    
    return response

